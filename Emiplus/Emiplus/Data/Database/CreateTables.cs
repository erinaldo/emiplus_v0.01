        private void CreateGenerator(string trigger, string tabela, string generator)
        {
            var SQLCon = new FbConnection(
                $"character set=NONE;initial catalog={_PathDB};user id={_user};data source={_host};user id={_db};Password={_pass};Pooling=true;Dialect=3"
            );

            FbCommand cmd;

            var query1 = "CREATE GENERATOR " + generator + ";";
            var query2 =
                $@"CREATE TRIGGER {trigger} FOR {tabela} ACTIVE BEFORE INSERT POSITION 0 AS DECLARE VARIABLE tmp DECIMAL(18,0); BEGIN IF (NEW.ID IS NULL) THEN NEW.ID = GEN_ID({generator}, 1); ELSE BEGIN tmp = GEN_ID({generator}, 0); if (tmp < new.ID) then tmp = GEN_ID({generator}, new.ID-tmp); END END";

            SQLCon.Open();
            cmd = new FbCommand(query1, SQLCon);
            cmd.ExecuteNonQuery();

            cmd = new FbCommand(query2, SQLCon);
            cmd.ExecuteNonQuery();
            SQLCon.Close();
        }

        public void CreateTable(string table)
        {
            dbLocal.Select("CREATE TABLE " + table + "(id integer generated by default as identity primary key); ");
        }

        public void CreateColumn(string table, string column, int type, int type_sub, int size)
        {
            string tipoCriar = "";
            if (type == 16 && type_sub == 1)
            {
                tipoCriar = "NUMERIC(18,4)";
            }
            else if (type == 8)
            {
                //integer
                tipoCriar = "INTEGER";
            }
            else if (type == 12)
            {
                //date
                tipoCriar = "DATE";
            }
            else if (type == 13)
            {
                //time
                tipoCriar = "TIME";
            }
            else if (type == 35)
            {
                //timestamp
                tipoCriar = "TIMESTAMP";
            }
            else if (type == 37)
            {
                //varchar
                var tamanhoColuna = size == 0 ? "255" : size.ToString();

                tipoCriar = "VARCHAR(" + tamanhoColuna + ")";
            }

            dbLocal.Select("ALTER TABLE " + table + " ADD " + column + " " + tipoCriar + ";");
        }

